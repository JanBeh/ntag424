static const uint8_t aes_rcon[10] = {
  0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1b, 0x36
};

void aes_key(aes_ctx_t *ctx, const uint8_t *key) {
  uint32_t tmp;
  uint8_t tmp0;
  memcpy(ctx->round_key, key, AES_KEY_LEN);
  for (int i=0; i<4*AES_ROUNDS; i++) {
    tmp = ctx->round_key[i+3];
    if (i%4 == 0) {
      tmp0 = ((uint8_t *)&tmp)[0];
      ((uint8_t *)&tmp)[0] = aes_sbox[((uint8_t *)&tmp)[1]];
      ((uint8_t *)&tmp)[1] = aes_sbox[((uint8_t *)&tmp)[2]];
      ((uint8_t *)&tmp)[2] = aes_sbox[((uint8_t *)&tmp)[3]];
      ((uint8_t *)&tmp)[3] = aes_sbox[tmp0];
      ((uint8_t *)&tmp)[0] ^= aes_rcon[i/4];
    }
    ctx->round_key[i+4] = ctx->round_key[i] ^ tmp;
  }
}

static void aes_add_round_key(
  uint32_t *state, const uint32_t *round_key, uint8_t round
) {
  for (int i=0; i<4; i++) state[i] ^= round_key[4*round+i];
}

static void aes_sub_bytes(uint8_t *state) {
  for (int i=0; i<16; i++) state[i] = aes_sbox[state[i]];
}

static void aes_inv_sub_bytes(uint8_t *state) {
  for (int i=0; i<16; i++) state[i] = aes_rsbox[state[i]];
}

static void aes_shift_rows(aes_matrix_t *state) {
  uint8_t tmp;
  tmp            = (*state)[0][1];
  (*state)[0][1] = (*state)[1][1];
  (*state)[1][1] = (*state)[2][1];
  (*state)[2][1] = (*state)[3][1];
  (*state)[3][1] = tmp;
  tmp            = (*state)[0][2];
  (*state)[0][2] = (*state)[2][2];
  (*state)[2][2] = tmp;
  tmp            = (*state)[1][2];
  (*state)[1][2] = (*state)[3][2];
  (*state)[3][2] = tmp;
  tmp            = (*state)[0][3];
  (*state)[0][3] = (*state)[3][3];
  (*state)[3][3] = (*state)[2][3];
  (*state)[2][3] = (*state)[1][3];
  (*state)[1][3] = tmp;
}

static void aes_inv_shift_rows(aes_matrix_t *state) {
  uint8_t tmp;
  tmp            = (*state)[3][1];
  (*state)[3][1] = (*state)[2][1];
  (*state)[2][1] = (*state)[1][1];
  (*state)[1][1] = (*state)[0][1];
  (*state)[0][1] = tmp;
  tmp            = (*state)[0][2];
  (*state)[0][2] = (*state)[2][2];
  (*state)[2][2] = tmp;
  tmp            = (*state)[1][2];
  (*state)[1][2] = (*state)[3][2];
  (*state)[3][2] = tmp;
  tmp            = (*state)[0][3];
  (*state)[0][3] = (*state)[1][3];
  (*state)[1][3] = (*state)[2][3];
  (*state)[2][3] = (*state)[3][3];
  (*state)[3][3] = tmp;
}

static uint8_t aes_xtime(uint8_t x) {
  return (x<<1) ^ (((x>>7) & 1) * 0x1B);
}

static void aes_mix_columns(aes_matrix_t *state) {
  uint8_t t0, t4, t1;
  for (int i=0; i<4; i++) {
    t0 = (*state)[i][0];
    t4 = (*state)[i][0] ^ (*state)[i][1] ^ (*state)[i][2] ^ (*state)[i][3];
    t1 = aes_xtime((*state)[i][0] ^ (*state)[i][1]);
    (*state)[i][0] ^= t1 ^ t4;
    t1 = aes_xtime((*state)[i][1] ^ (*state)[i][2]);
    (*state)[i][1] ^= t1 ^ t4;
    t1 = aes_xtime((*state)[i][2] ^ (*state)[i][3]);
    (*state)[i][2] ^= t1 ^ t4;
    t1 = aes_xtime((*state)[i][3] ^ t0);
    (*state)[i][3] ^= t1 ^ t4; 
  }
}

static uint8_t aes_multiply(uint8_t x, uint8_t y) {
  return
    ((y & 1) * x) ^
    ((y>>1 & 1) * aes_xtime(x)) ^
    ((y>>2 & 1) * aes_xtime(aes_xtime(x))) ^
    ((y>>3 & 1) * aes_xtime(aes_xtime(aes_xtime(x))));
}

static void aes_inv_mix_columns(aes_matrix_t *state) {
  uint8_t a, b, c, d;
  for (int i=0; i<4; i++) {
    a = (*state)[i][0];
    b = (*state)[i][1];
    c = (*state)[i][2];
    d = (*state)[i][3];
    (*state)[i][0] =
      aes_multiply(a, 0x0e) ^ aes_multiply(b, 0x0b) ^
      aes_multiply(c, 0x0d) ^ aes_multiply(d, 0x09);
    (*state)[i][1] =
      aes_multiply(a, 0x09) ^ aes_multiply(b, 0x0e) ^
      aes_multiply(c, 0x0b) ^ aes_multiply(d, 0x0d);
    (*state)[i][2] =
      aes_multiply(a, 0x0d) ^ aes_multiply(b, 0x09) ^
      aes_multiply(c, 0x0e) ^ aes_multiply(d, 0x0b);
    (*state)[i][3] =
      aes_multiply(a, 0x0b) ^ aes_multiply(b, 0x0d) ^
      aes_multiply(c, 0x09) ^ aes_multiply(d, 0x0e);
  }
}

void aes_enc(const aes_ctx_t *ctx, void *buf) {
  aes_add_round_key((uint32_t *)buf, ctx->round_key, 0);
  for (int i=1; ; i++) {
    aes_sub_bytes((uint8_t *)buf);
    aes_shift_rows((aes_matrix_t *)buf);
    if (i == AES_ROUNDS) break;
    aes_mix_columns((aes_matrix_t *)buf);
    aes_add_round_key((uint32_t *)buf, ctx->round_key, i);
  }
  aes_add_round_key((uint32_t *)buf, ctx->round_key, AES_ROUNDS);
}

void aes_dec(const aes_ctx_t *ctx, void *buf) {
  aes_add_round_key((uint32_t *)buf, ctx->round_key, AES_ROUNDS);
  for (int i=AES_ROUNDS-1; ; i--) {
    aes_inv_shift_rows((aes_matrix_t *)buf);
    aes_inv_sub_bytes((uint8_t *)buf);
    aes_add_round_key((uint32_t *)buf, ctx->round_key, i);
    if (i == 0) break;
    aes_inv_mix_columns((aes_matrix_t *)buf);
  }
}
